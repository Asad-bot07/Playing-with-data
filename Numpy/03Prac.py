import numpy as np

arr = np.array((1, 2, 3, 4, 5, 6, 10))

arr2 = np.array((2, 1, 5, 6, 7, 8, 9))

# Addition
print(arr + arr2)

# Multiplication
print(arr * arr2)

# substraction
print(arr - arr2)

#division
print(arr / arr2)

x = np.array((4, 9, 16, 25))

# sqrt
print(np.sqrt(x))

#sum of all elements
print(np.sum(np.sqrt(x)))

#mean of the array
print(np.mean(x))

#max of the array
print(np.max(x))

#min of the array
print(np.min(x))